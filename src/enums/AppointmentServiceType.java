package enums;

/**
 * Enum representing various types of appointment services available.
 * Each service type defines a specific category of medical procedure or examination.
 * 
 * @author Natalyn Pong 
 * @version 1.0
 */
public enum AppointmentServiceType {
    /** General consultation service for routine check-ups or initial diagnosis. */
    CONSULTATION,

    /** Service for X-ray imaging to diagnose or monitor internal conditions. */
    XRAY,

    /** Blood test service for lab analysis of blood samples. */
    BLOOD_TEST,

    /** Regular check-up service for monitoring general health status. */
    CHECKUP,

    /** Surgical procedure service for more invasive treatments or operations. */
    SURGERY
}
